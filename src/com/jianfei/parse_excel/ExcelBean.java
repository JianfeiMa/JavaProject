package com.jianfei.parse_excel;

public class ExcelBean {
    private String collaborateName;
    private String latGd;
    private String lngGd;
    private String lat;
    private String lng;

    public String getCollaborateName() {
        return collaborateName;
    }

    public void setCollaborateName(String collaborateName) {
        this.collaborateName = collaborateName;
    }

    public String getLatGd() {
        return latGd;
    }

    public void setLatGd(String latGd) {
        this.latGd = latGd;
    }

    public String getLngGd() {
        return lngGd;
    }

    public void setLngGd(String lngGd) {
        this.lngGd = lngGd;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }
}
